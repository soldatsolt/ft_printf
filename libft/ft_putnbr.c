#include "libft.h"

void	ft_putnbr(int n)
{
	long	m;
	long	nb;

	nb = n;
	m = 1;
	if (nb < 0)
	{
		nb = nb * (-1);
		ft_putchar('-');
	}
	while (nb / (m * 10) > 0)
		m = m * 10;
	while (m > 0)
	{
		ft_putchar((nb / m + '0'));
		nb = nb % m;
		m = m / 10;
	}
}
