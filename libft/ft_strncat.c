#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	isize;

	i = 0;
	isize = 0;
	while (*s1)
	{
		s1++;
		i++;
	}
	while (*s2 && isize < n)
	{
		*s1 = *s2;
		s1++;
		s2++;
		i++;
		isize++;
	}
	*s1 = '\0';
	s1 = s1 - i;
	return (s1);
}
